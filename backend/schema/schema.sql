-- MBTI Career NEU: __SCHEMA__ được thay bằng tên schema (ví dụ "mbti_career") khi chạy
CREATE SCHEMA IF NOT EXISTS __SCHEMA__;

-- 16 nhóm MBTI tham chiếu
CREATE TABLE IF NOT EXISTS __SCHEMA__.mbti_types (
  id   integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  code varchar(4) NOT NULL UNIQUE
);

